public class Categoria {

    private long id;
    private String name;
    private String description;

    public Long setId() {
        return id;
    }
    public Long getId() {
        return id;
    }

    public String setName() {
        return name;
    }

    public String getName() {
        return name;
    }

    public String setDescription() {
        return description;
    }

    public String getDescription() {
        return description;
    }


}
